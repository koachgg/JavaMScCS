public class OperatorsDemo {
    public static void main(String[] args) {
    int myadd  = 12+16;
    System.out.println("Addition: " +myadd);

    int mysub = 16-12;
    System.out.println("Subtraction : "+mysub);

    int mymul = 16*12;
    System.out.println("Multiplication : "+mymul);

    int mydiv = 16/12;
    System.out.println(mydiv);
    
    int mymod = 16 % 12;
    System.out.println(mymod);
    }
    
}
