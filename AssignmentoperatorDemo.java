public class AssignmentoperatorDemo {
    public static void main(String[] args) {
        int mynum = 12;
        mynum += 16;
        System.out.println("Addition : " + mynum);

        mynum = 12;
        mynum -= 16;
        System.out.println("Subtraction : " + mynum);

        mynum = 12;
        mynum *= 16;
        System.out.println("Multilication : " + mynum);

        mynum = 12;
        mynum /= 16;
        System.out.println("Division :" + mynum);

        mynum = 12;
        mynum %= 16;
        System.out.println("Mod:" + mynum);

    }
}
